//Write a program to swap two numbers.

class swap2num
{
	public static void main(String [] args)
	{
		int num1=21, num2=12, num3;
		num3=num1;
		num1=num2;
		num2=num3;
		System.out.println("After swapping the 1st number:" +num1);
		System.out.println("After swapping the 2nd number:" +num2);
	}
}